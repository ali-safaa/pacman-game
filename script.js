function createMap() {
     for (let x = 0; x < map[0].length; x++) {
          for (let y = 0; y < map.length; y++) {
               if (map[x][y] == 1) {
                    ctx.strokeStyle = "#00FFFF";
                    ctx.strokeRect(y * 30, x * 30, 30, 30);
               }
          }
     }
}

createMap();

function createGhosts() {
     for (let i = 0; i < ghostImageLocations.length; i++) {
          ctx.fillStyle = "red";
          ctx.beginPath();
          ctx.arc(
               ghostImageLocations[i].x,
               ghostImageLocations[i].y,
               10,
               0,
               Math.PI * 2
          );
          ctx.fill();
     }
}

